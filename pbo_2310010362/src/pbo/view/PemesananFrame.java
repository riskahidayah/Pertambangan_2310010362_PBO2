    private void tblPemesananMouseClicked(java.awt.event.MouseEvent evt) {                                   
        int baris = tblPemesanan.getSelectedRow();
        
        if (baris != -1) {
            enableField(true);
            txtIdPemesanan.setEditable(false);
            
            DefaultTableModel model = (DefaultTableModel) tblPemesanan.getModel();
            
            txtIdPemesanan.setText(model.getValueAt(baris, 0).toString());
            txtTanggal.setText(model.getValueAt(baris, 1).toString());
            jTextArea1.setText(model.getValueAt(baris, 4).toString());
            
            // Set ComboBox berdasarkan Nama yang ditampilkan di tabel
            String namaKonsumenTabel = model.getValueAt(baris, 2).toString();
            String namaKaryawanTabel = model.getValueAt(baris, 3).toString();

            // Mencari item di ComboBox yang mengandung Nama yang sesuai
            for (int i = 0; i < cmbKonsumen.getItemCount(); i++) {
                if (cmbKonsumen.getItemAt(i).contains(namaKonsumenTabel)) {
                    cmbKonsumen.setSelectedIndex(i);
                    break;
                }
            }

            for (int i = 0; i < cmbKaryawan.getItemCount(); i++) {
                if (cmbKaryawan.getItemAt(i).contains(namaKaryawanTabel)) {
                    cmbKaryawan.setSelectedIndex(i);
                    break;
                }
            }
            
            btnSimpan.setEnabled(false);
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
        }
    }
    
