    private void dataKolom(){
            model = new DefaultTableModel();
            model.addColumn("ID Karyawan");
            model.addColumn("Nama");
            model.addColumn("Jabatan");
            model.addColumn("Alamat");
            model.addColumn("No. Telp");
            model.addColumn("Password");
            jTable1.setModel(model);
    }
    
    private void tampilData(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection conn = pbo.koneksi.Koneksi.getKoneksi();
            String sql = "SELECT * FROM KARYAWAN";
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                Object[] obj = new Object[6];
                obj[0] = res.getString("ID_Karyawan");
                obj[1] = res.getString("Nama_Karyawan");
                obj[2] = res.getString("Jabatan");
                obj[3] = res.getString("Alamat_Karyawan");
                obj[4] = res.getString("No_telp_Karyawan");
                obj[5] = res.getString("Password");
                
                model.addRow(obj);
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error Tampil Data:" + e.getMessage());
        }
    }
    
