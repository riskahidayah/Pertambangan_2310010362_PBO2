    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String idKonsumen = pemesanan.getIdFromCmb(cmbKonsumen);
        String idKaryawan = pemesanan.getIdFromCmb(cmbKaryawan);
        String id = txtIdPemesanan.getText().trim();
        String tgl = txtTanggal.getText().trim();
        String notes = jTextArea1.getText().trim(); 

        
        if (txtIdPemesanan.getText().isEmpty() || txtTanggal.getText().isEmpty() || 
        idKonsumen.isEmpty() || idKaryawan.isEmpty()) {
       // JOptionPane.showMessageDialog(null, "ID Pemesanan, Tanggal, Konsumen, dan Karyawan harus diisi!");
        return;
        }

       
        pemesanan.simpanPemesanan(id, tgl, idKonsumen, idKaryawan, notes);

      
        if (pemesanan.validasi) {
            tampilData();
            bersih();
            enableField(false);
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        if (txtIdPemesanan.getText().isEmpty()) {
           // JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah terlebih dahulu!");
            return;
        }
        
        String idKonsumen = pemesanan.getIdFromCmb(cmbKonsumen);
        String idKaryawan = pemesanan.getIdFromCmb(cmbKaryawan);
        
        if (idKonsumen == null || idKaryawan == null) {
            JOptionPane.showMessageDialog(null, "Konsumen atau Karyawan tidak valid!");
            return;
        }
        
        String id = txtIdPemesanan.getText();
        String tgl = txtTanggal.getText();
        String notes = jTextArea1.getText();
        
        pemesanan.ubahPemesanan(id, tgl, idKonsumen, idKaryawan, notes);
        tampilData();
        bersih();
        enableField(false);
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (txtIdPemesanan.getText().isEmpty()) {
        //    JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus terlebih dahulu!");
            return;
        }
        
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data Pemesanan ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION) {
            pemesanan.hapusPemesanan(txtIdPemesanan.getText());
            tampilData();
            bersih();
            enableField(false);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

