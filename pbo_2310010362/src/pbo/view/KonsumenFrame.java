    private void dataKolom(){
            model = new DefaultTableModel();
            model.addColumn("ID_Konsumen");
            model.addColumn("Nama Perusahaan");
            model.addColumn("Alamat Perusahaan");
            model.addColumn("No. Telp");
            jTable1.setModel(model);
    }
    
    private void tampilData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection conn = pbo.koneksi.Koneksi.getKoneksi();
            String sql = "SELECT * FROM KONSUMEN";
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                Object[] obj = new Object[4];
                obj[0] = res.getString("ID_Konsumen");
                obj[1] = res.getString("Nama_Perusahaan");
                obj[2] = res.getString("Alamat_Perusahaan");
                obj[3] = res.getString("No_telp_Perusahaan");
                
                model.addRow(obj);
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error Tampil Data:" + e.getMessage());
        }
    }
    
