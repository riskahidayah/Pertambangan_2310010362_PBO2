            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.setString(1, txtIdKaryawan.getText());
            pst.setString(2, txtNama.getText());
            pst.setString(3, txtJabatan.getText());
            pst.setString(4, txtAlamat.getText());
