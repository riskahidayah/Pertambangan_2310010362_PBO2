    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = pbo.koneksi.Koneksi.getKoneksi();
            String sql = "INSERT INTO konsumen VALUES (?,?,?,?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.setString(1, txtIdKonsumen.getText());
            pst.setString(2, txtNamaPerusahaan.getText());
            pst.setString(3, txtAlamatPerusahaan.getText());
            pst.setString(4, txtTelpPerusahaan.getText());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Konsumen Berhasil Disimpan");
            
            tampilData();
            bersih();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Gagal:" + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

