    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String idDetail = txtIdDetailPemesanan.getText().trim();
    
    if (idDetail.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus terlebih dahulu!");
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(null, 
        "Yakin ingin menghapus data Detail Pemesanan dengan ID " + idDetail + " ?", 
        "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    
    if (konfirmasi == JOptionPane.YES_OPTION) {
        detailPemesanan.hapusDetail(idDetail);
        tampilDataDetail();
        bersih();
        enableField(false);
        System.out.println("Data dengan ID " + idDetail + " telah dihapus!");
    }
    }//GEN-LAST:event_btnHapusActionPerformed

