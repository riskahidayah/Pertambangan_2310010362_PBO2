    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txtIdKonsumen.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data konsumen yang akan dihapus dari tabel!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; 
        }
        
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin Ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try{
                Connection conn = pbo.koneksi.Koneksi.getKoneksi();
                String sql = "DELETE FROM konsumen WHERE TRIM(ID_Konsumen)=?";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                
                pst.setString(1, txtIdKonsumen.getText().trim());
                int rowsAffected = pst.executeUpdate();
                
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Data Konsumen Berhasil Dihapus");
                } else {
                    JOptionPane.showMessageDialog(null, "Penghapusan Data Gagal. ID Konsumen tidak ditemukan.", "Error", JOptionPane.ERROR_MESSAGE);
                }
                
                tampilData();
                bersih();
                txtIdKonsumen.setEditable(true);            
            }catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Penghapusan Data Gagal:" + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

