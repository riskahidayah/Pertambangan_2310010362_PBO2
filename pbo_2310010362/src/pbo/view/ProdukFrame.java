    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                   
        int baris = jTable1.getSelectedRow();
        
       
        if (baris != -1) {
          
            enableField(true);
            txtIdProduk.setEditable(false); 

           
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            txtIdProduk.setText(model.getValueAt(baris, 0).toString());
            txtJenisProduk.setText(model.getValueAt(baris, 1).toString());
            txtUkuran.setText(model.getValueAt(baris, 2).toString());
            txtHarga.setText(model.getValueAt(baris, 3).toString());
            
            btnSimpan.setEnabled(false); 
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
        }
    }
    
    
