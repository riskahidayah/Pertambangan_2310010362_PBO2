    private void btnBatalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBatalActionPerformed
        // TODO add your handling code here:
        if (btnSimpan.isEnabled() || btnUbah.isEnabled() || btnHapus.isEnabled()) {
            bersih();
            enableField(false);
        } else {
            enableField(true); 
            bersih();
            txtIdProduk.requestFocus();
        }
    }//GEN-LAST:event_btnBatalActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtIdProduk.getText().isEmpty() || txtJenisProduk.getText().isEmpty() || txtUkuran.getText().isEmpty() || txtHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
            return;
        }

        try {
            String id = txtIdProduk.getText();
            String jenis = txtJenisProduk.getText();
            String ukr = txtUkuran.getText();
            double hrg = Double.parseDouble(txtHarga.getText());

            produk.simpanProduk(id, jenis, ukr, hrg);

            if (produk.validasi) {
                tampilData();
                bersih();
                enableField(false);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Format Harga Tidak Valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        if (txtIdProduk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah terlebih dahulu!");
            return;
        }
        
        try {
            String id = txtIdProduk.getText();
            String jenis = txtJenisProduk.getText();
            String ukr = txtUkuran.getText();
            double hrg = Double.parseDouble(txtHarga.getText());
            
            produk.ubahProduk(id, jenis, ukr, hrg);
            tampilData();
            bersih();
            enableField(false);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Format Harga Tidak Valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (txtIdProduk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus terlebih dahulu!");
            return;
        }
        
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION) {
            produk.hapusProduk(txtIdProduk.getText());
            tampilData();
            bersih();
            enableField(false);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

