    private void btnTambahItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahItemActionPerformed
        // TODO add your handling code here:
        String idPemesanan = (String) cmbPemesanan.getSelectedItem();
        String idProduk = detailPemesanan.getIdProdukFromCmb(cmbProduk);
        String hargaStr = txtHarga.getText();
        String jumlahStr = txtJumlah.getText();
        String status = (String) cmbStatus.getSelectedItem();

        // Validasi Wajib Isi
        if (idPemesanan == null || idPemesanan.equals("-- Pilih ID Pemesanan --") || idProduk == null || txtIdDetailPemesanan.getText().isEmpty() || jumlahStr.isEmpty() || status == null || status.equals("-- Pilih Status --")) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
            return;
        }

        try {
            int harga = Integer.parseInt(hargaStr);
            int jumlah = Integer.parseInt(jumlahStr);
            
            detailPemesanan.simpanDetail(txtIdDetailPemesanan.getText(), idPemesanan, idProduk, harga, jumlah, status);
            
            if (detailPemesanan.validasi) {
                tampilDataDetail();
                bersih();
            }
        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(null, "Harga dan Jumlah harus berupa angka!");
        }
    }//GEN-LAST:event_btnTambahItemActionPerformed

